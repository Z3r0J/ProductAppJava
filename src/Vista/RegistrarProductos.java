    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int n = JOptionPane.showConfirmDialog(
                this,
                "Â¿Estas seguro de eliminar este producto?",
                "Alerta",
                JOptionPane.YES_NO_OPTION);
        ClaseProductos prod = new ClaseProductos();
        ServicioProductos sp = new ServicioProductos();

        prod.IdProductos = ID;

        if (n == JOptionPane.YES_OPTION) {
            if (sp.eliminarProductos(prod) == true) {
                JOptionPane.showMessageDialog(this, "Eliminado Correctamente",
                        "Alerta", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                ListadoProducto list = new ListadoProducto();
                list.LlenarTabla();
                list.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Ocurrio un error",
                        "Error", JOptionPane.ERROR_MESSAGE);
                ListadoProducto list = new ListadoProducto();
                list.LlenarTabla();
                list.setVisible(true);
            }
        } else {

        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        if (TFNombre.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Inserta un nombre", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (TFMarca.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Inserta una Marca", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (TFCategoria.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Inserta una Categoria", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (TFPrecio.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Inserta un precio", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (TFStock.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Inserta un stock", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (editarse == true) {
                editarProductos();
                LimpiarCampos();
                ListadoProducto list = new ListadoProducto();
                list.LlenarTabla();
                this.dispose();
                list.setVisible(true);
            } else {
                agregarProductos();
                LimpiarCampos();
                ListadoProducto list = new ListadoProducto();
                list.LlenarTabla();
                this.dispose();
                list.setVisible(true);
            }
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed
    private void LimpiarCampos() {
        TFNombre.setText("");
        TFMarca.setText("");
        TFPrecio.setText("");
        TFCategoria.setText("");
        TFStock.setText("");
    }

    private void editarProductos() {
        ClaseProductos prod = new ClaseProductos();
        ServicioProductos sp = new ServicioProductos();
        prod.IdProductos = ID;
        prod.NombreProductos = TFNombre.getText();
        prod.MarcaProductos = TFMarca.getText();
        prod.CategoriaProductos = TFCategoria.getText();
        prod.PrecioProducto = Integer.valueOf(TFPrecio.getText());
        prod.StockProducto = Integer.valueOf(TFStock.getText());
        sp.editarProducto(prod);
        LimpiarCampos();
    }

    private void agregarProductos() {
        BtnEliminar.setVisible(false);
        BtnGuardar.setText("Guardar");
        ClaseProductos prod = new ClaseProductos();
        ServicioProductos sp = new ServicioProductos();
        prod.IdProductos = 0;
        prod.NombreProductos = TFNombre.getText();
        prod.MarcaProductos = TFMarca.getText();
        prod.CategoriaProductos = TFCategoria.getText();
        prod.PrecioProducto = Integer.valueOf(TFPrecio.getText());
        prod.StockProducto = Integer.valueOf(TFStock.getText());
        sp.registroProductos(prod);
        LimpiarCampos();
    }

